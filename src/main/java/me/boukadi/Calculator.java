package me.boukadi;

public class Calculator {

    public int add(int a, int b) {
        return a+b;
    }

    public double circleArea(double radius) {
        return Math.PI * radius * radius;
    }

    public float divide(int a, int b) { return a/b; };

    public int multiply(int a, int b) {
        return a*b;
    }
}
